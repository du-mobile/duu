}

function decrementCartItem(title) {
const cartItem = cartItemList.find(item => item.title === title);
if (cartItem && cartItem.quantity > 0) {  
cartItem.quantity -= 1;
if (cartItem.quantity === 0) {
  removeCartItem(title);
  if (notificationBar){
       notificationBar.style.display = "none";  
     }
}else{
  showNotificationBar(); 
}
updateCartUI();

}
}

