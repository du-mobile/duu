<p>نحن نحترم حقوق الملكية الفكرية للآخرين. إذا كنت تعتقد أن أي مادة متاحة على الخدمات أو من خلالها تنتهك أي حقوق نشر تمتلكها أو تتحكم فيها ، فيرجى إخطار وكيل حقوق الطبع والنشر المعين لدينا على الفور باستخدام معلومات الاتصال الواردة أدناه ("الإخطار"). سيتم إرسال نسخة من الإشعار الخاص بك إلى الشخص الذي نشر أو خزن المواد التي تم تناولها في الإشعار. يرجى العلم أنه وفقا للقانون الفيدرالي ، قد تكون مسؤولا عن الأضرار إذا قدمت تحريفات جوهرية في الإخطار. وبالتالي ، إذا لم تكن متأكدا من أن المواد الموجودة على الخدمات أو المرتبطة بها تنتهك حقوق الطبع والنشر الخاصة بك ، فيجب أن تفكر أولا في الاتصال بمحام.</p>

<p>يجب أن تفي جميع الإخطارات بمتطلبات قانون الألفية الجديدة لحقوق طبع ونشر المواد الرقمية 17 U.S.C. § 512 (c) (3) وأن تتضمن المعلومات التالية: (1) توقيع مادي أو إلكتروني لشخص مخول بالتصرف نيابة عن مالك الحق الحصري المزعوم انتهاكه ؛ (2) تحديد العمل المحمي بحقوق الطبع والنشر المزعوم انتهاكه ، أو ، إذا كان الإخطار يغطي العديد من الأعمال المحمية بحقوق الطبع والنشر على الخدمات ، قائمة تمثيلية لهذه الأعمال على الخدمات ؛ (3) تحديد المواد التي يزعم أنها تنتهك أو تخضع لنشاط الانتهاك والتي يجب إزالتها أو تعطيل الوصول إليها ، ومعلومات كافية بشكل معقول للسماح لنا بتحديد موقع المواد ؛ (4) معلومات كافية بشكل معقول للسماح لنا بالاتصال بالطرف المشتكي ، مثل العنوان ورقم الهاتف ، وإذا كان متاحا ، عنوان بريد إلكتروني يمكن الاتصال بالطرف المشتكي عليه ؛ (5) بيان بأن الطرف المشتكي لديه اعتقاد حسن النية بأن استخدام المواد بالطريقة المشكو منها غير مصرح به من قبل مالك حقوق الطبع والنشر أو وكيله أو القانون ؛ و (6) بيان بأن المعلومات الواردة في الإخطار دقيقة ، وتحت طائلة عقوبة الحنث باليمين ، وأن الطرف المشتكي مخول بالتصرف نيابة عن مالك الحق الحصري المزعوم انتهاكه.</p>

<p>الإخطار المضاد</p>

<p>إذا كنت تعتقد أن المواد المحمية بحقوق الطبع والنشر الخاصة بك قد تمت إزالتها من الخدمات نتيجة لخطأ أو خطأ في تحديد الهوية ، فيمكنك إرسال إشعار مضاد كتابي إلى [نحن / وكيل حقوق الطبع والنشر المعين لدينا] باستخدام معلومات الاتصال الواردة أدناه ("إشعار مضاد"). لكي يكون إشعارا مضادا فعالا بموجب قانون الألفية الجديدة لحقوق طبع ونشر المواد الرقمية ، يجب أن يتضمن الإشعار المضاد الخاص بك ما يلي بشكل كبير: (1) تحديد المواد التي تمت إزالتها أو تعطيلها والموقع الذي ظهرت فيه المادة قبل إزالتها أو تعطيلها ؛ (2) بيان بأنك توافق على اختصاص محكمة المقاطعة الفيدرالية التي يقع فيها عنوانك ، أو إذا كان عنوانك خارج الولايات المتحدة ، لأي منطقة قضائية نتواجد فيها ؛ (3) بيان بأنك ستقبل خدمة الإجراءات من الطرف الذي قدم الإخطار أو وكيل الطرف ؛ (4) اسمك وعنوانك ورقم هاتفك؛ (5) بيان تحت طائلة عقوبة الحنث باليمين بأن لديك اعتقاد حسن النية بأن المادة المعنية قد تمت إزالتها أو تعطيلها نتيجة لخطأ أو خطأ في تحديد المادة المراد إزالتها أو تعطيلها ؛ و (6) توقيعك المادي أو الإلكتروني.</p>

<p>إذا أرسلت إلينا إشعارا مضادا صالحا ومكتوبا يفي بالمتطلبات الموضحة أعلاه ، فسنقوم باستعادة المواد التي تمت إزالتها أو تعطيلها ، ما لم نتلق أولا إشعارا من الطرف الذي قدم الإخطار يخبرنا بأن هذا الطرف قد رفع دعوى قضائية لمنعك من الانخراط في نشاط مخالف يتعلق بالمواد المعنية. يرجى ملاحظة أنه إذا قدمت إقرارا مضللا ماديا بأن المحتوى المعطل أو الذي تمت إزالته قد تمت إزالته عن طريق الخطأ أو التعريف الخاطئ ، فقد تكون مسؤولا عن الأضرار ، بما في ذلك التكاليف وأتعاب المحاماة. يشكل تقديم إشعار مضاد كاذب شهادة زور.</p>

