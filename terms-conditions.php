<p>قد تدعوك الخدمات للدردشة أو المساهمة أو المشاركة في المدونات ولوحات الرسائل والمنتديات عبر الإنترنت والوظائف الأخرى ، وقد توفر لك الفرصة لإنشاء أو إرسال أو نشر أو عرض أو نقل أو أداء أو نشر أو توزيع أو بث المحتوى والمواد إلينا أو على الخدمات ، بما في ذلك على سبيل المثال لا الحصر النصوص والكتابات والفيديو والصوت ،  الصور الفوتوغرافية أو الرسومات أو التعليقات أو الاقتراحات أو المعلومات الشخصية أو المواد الأخرى (يشار إليها مجتمعة باسم "المساهمات"). قد تكون المساهمات قابلة للعرض من قبل مستخدمين آخرين للخدمات ومن خلال مواقع ويب تابعة لجهات خارجية. على هذا النحو ، قد يتم التعامل مع أي مساهمات ترسلها على أنها غير سرية وغير مملوكة. عند إنشاء أو إتاحة أي مساهمات ، فإنك بذلك تقر وتضمن ما يلي:</p>
<ul>
    <li>إن إنشاء مساهماتك أو توزيعها أو نقلها أو عرضها للجمهور أو أدائها والوصول إليها أو تنزيلها أو نسخها لا ينتهك ولن ينتهك حقوق الملكية ، بما في ذلك على سبيل المثال لا الحصر حقوق الطبع والنشر أو براءات الاختراع أو العلامات التجارية أو الأسرار التجارية أو الحقوق المعنوية لأي طرف ثالث.</li>
    <li>أنت منشئ ومالك أو لديك التراخيص والحقوق والموافقات والإصدارات والأذونات اللازمة لاستخدام وتفويضنا وتفويض الخدمات والمستخدمين الآخرين للخدمات لاستخدام مساهماتك بأي طريقة متوخاة في الخدمات وهذه الشروط القانونية.</li>
    <li>لديك موافقة كتابية و / أو إعفاء و / أو إذن من كل شخص يمكن التعرف عليه في مساهماتك لاستخدام اسم أو شكل كل شخص يمكن التعرف عليه لتمكين تضمين واستخدام مساهماتك بأي طريقة متوخاة في الخدمات وهذه الشروط القانونية.</li>
    <li>مساهماتك ليست خاطئة أو غير دقيقة أو مضللة.</li>
    <li>مساهماتك ليست إعلانات غير مرغوب فيها أو غير مصرح بها أو مواد ترويجية أو مخططات هرمية أو رسائل متسلسلة أو بريد عشوائي أو رسائل بريدية جماعية أو أشكال أخرى من الالتماس.</li>
    <li>مساهماتك ليست فاحشة أو بذيئة أو فاسقة أو قذرة أو عنيفة أو مضايقة أو تشهيرية أو افترائية أو مرفوضة بأي شكل آخر (على النحو الذي نحدده).</li>
    <li>مساهماتك لا تسخر أو تسخر أو تحط من قدر أو تخيف أو تسيء إلى أي شخص.</li>
    <li>لا تستخدم مساهماتك لمضايقة أو تهديد (بالمعنى القانوني لهذه المصطلحات) أي شخص آخر والترويج للعنف ضد شخص معين أو فئة معينة من الناس.</li>
    <li>لا تنتهك مساهماتك أي قانون أو لائحة أو قاعدة معمول بها.</li>
    <li>لا تنتهك مساهماتك حقوق الخصوصية أو الدعاية لأي طرف ثالث.</li>
    <li>لا تنتهك مساهماتك أي قانون معمول به يتعلق باستغلال الأطفال في المواد الإباحية ، أو تهدف إلى حماية صحة أو رفاهية القصر.</li>
    <li>لا تتضمن مساهماتك أي تعليقات مسيئة مرتبطة بالعرق أو الأصل القومي أو الجنس أو التفضيل الجنسي أو الإعاقة الجسدية.</li>
    <li>لا تنتهك مساهماتك أو ترتبط بمواد تنتهك أي شرط من هذه الشروط القانونية أو أي قانون أو لائحة معمول بها.</li>
</ul>

<p>أي استخدام للخدمات ينتهك ما سبق ينتهك هذه الشروط القانونية وقد يؤدي ، من بين أمور أخرى ، إلى إنهاء أو تعليق حقوقك في استخدام الخدمات.</p>

<h3 id="license">11. رخصة المساهمة</h3>

<p>من خلال نشر مساهماتك في أي جزء من الخدمات أو جعل المساهمات في متناول الخدمات عن طريق ربط حسابك من الخدمات بأي من حسابات الشبكات الاجتماعية الخاصة بك ، فإنك تمنح تلقائيا ، وتقر وتضمن أن لديك الحق في منحنا غير مقيد وغير محدود وغير قابل للإلغاء ودائم وغير حصري وقابل للتحويل وخالي من حقوق الملكية ،  حق عالمي مدفوع بالكامل وترخيص لاستضافة واستخدام ونسخ وإعادة إنتاج والكشف عن وبيع وإعادة بيع ونشر وبث وإعادة عنوان وأرشفة وتخزين وتخزين وأداء علني وعرض علني وإعادة تنسيق وترجمة ونقل ومقتطفات (كليا أو جزئيا) ، وتوزيع هذه المساهمات (بما في ذلك ، على سبيل المثال لا الحصر ، صورتك وصوتك) لأي غرض ، تجاري أو إعلاني أو غير ذلك ، ولإعداد أعمال مشتقة من ،  أو دمجها في أعمال أخرى ، مثل هذه المساهمات ، ومنح وتفويض التراخيص الفرعية لما سبق. قد يحدث الاستخدام والتوزيع بأي تنسيقات وسائط ومن خلال أي قنوات وسائط.</p>

<p>ينطبق هذا الترخيص على أي شكل أو وسائط أو تقنية معروفة الآن أو يتم تطويرها فيما بعد ، ويتضمن استخدامنا لاسمك واسم شركتك واسم الامتياز ، حسب الاقتضاء ، وأي من العلامات التجارية وعلامات الخدمة والأسماء التجارية والشعارات والصور الشخصية والتجارية التي تقدمها. أنت تتنازل عن جميع الحقوق المعنوية في مساهماتك ، وتضمن أن الحقوق المعنوية لم يتم تأكيدها بطريقة أخرى في مساهماتك.</p>

<p>نحن لا نؤكد أي ملكية لمساهماتك. أنت تحتفظ بالملكية الكاملة لجميع مساهماتك وأي حقوق ملكية فكرية أو حقوق ملكية أخرى مرتبطة بمساهماتك. نحن لسنا مسؤولين عن أي بيانات أو إقرارات في مساهماتك التي قدمتها في أي منطقة على الخدمات. أنت وحدك المسؤول عن مساهماتك في الخدمات وتوافق صراحة على إعفائنا من أي وجميع المسؤوليات والامتناع عن أي إجراء قانوني ضدنا فيما يتعلق بمساهماتك.</p>

<p>لدينا الحق ، وفقا لتقديرنا الخاص والمطلق ، (1) تحرير أو تنقيح أو تغيير أي مساهمات ؛ (2) لإعادة تصنيف أي مساهمات لوضعها في مواقع أكثر ملاءمة على الخدمات ؛ و (3) الفحص المسبق أو حذف أي مساهمات في أي وقت ولأي سبب ، دون إشعار. ليس لدينا أي التزام بمراقبة مساهماتك.</p>

<h3 id="reviews">12. إرشادات للمراجعات</h3>

<p>قد نوفر لك مناطق على الخدمات لترك التعليقات أو التقييمات. عند نشر مراجعة ، يجب أن تمتثل للمعايير التالية: (1) يجب أن يكون لديك خبرة مباشرة مع الشخص / الكيان الذي تتم مراجعته ؛ (2) يجب ألا تحتوي مراجعاتك على ألفاظ نابية مسيئة أو لغة مسيئة أو عنصرية أو مسيئة أو تحض على الكراهية؛ (3) يجب ألا تحتوي مراجعاتك على مراجع تمييزية على أساس الدين أو العرق أو الجنس أو الأصل القومي أو العمر أو الحالة الاجتماعية أو التوجه الجنسي أو الإعاقة ؛ (4) يجب ألا تحتوي مراجعاتك على إشارات إلى نشاط غير قانوني ؛ (5) يجب ألا تكون تابعا للمنافسين في حالة نشر مراجعات سلبية ؛ (6) يجب ألا تقدم أي استنتاجات بشأن شرعية السلوك ؛ (7) لا يجوز لك نشر أي بيانات كاذبة أو مضللة ؛ و (8) لا يجوز لك تنظيم حملة تشجع الآخرين على نشر التعليقات، سواء كانت إيجابية أو سلبية.</p>

<p>يجوز لنا قبول المراجعات أو رفضها أو إزالتها وفقا لتقديرنا الخاص. ليس لدينا أي التزام على الإطلاق بفحص المراجعات أو حذفها ، حتى إذا اعتبر أي شخص المراجعات مرفوضة أو غير دقيقة. لا يتم اعتماد المراجعات من قبلنا ، ولا تمثل بالضرورة آرائنا أو آراء أي من الشركات التابعة لنا أو شركائنا. نحن لا نتحمل المسؤولية عن أي مراجعة أو عن أي مطالبات أو التزامات أو خسائر ناتجة عن أي مراجعة. من خلال نشر مراجعة ، فإنك تمنحنا بموجب هذا حقا وترخيصا دائما وغير حصري وعالمي وخالي من حقوق الملكية ومدفوع بالكامل وقابل للتنازل وقابل للترخيص من الباطن لإعادة إنتاج وتعديل وترجمة ونقل بأي وسيلة وعرض وأداء و / أو توزيع جميع المحتويات المتعلقة بالمراجعة.</p>

<h3 id="socialmedia">13. وسائل التواصل الاجتماعي</h3>

<p>كجزء من وظائف الخدمات ، يمكنك ربط حسابك بالحسابات عبر الإنترنت التي لديك مع موفري خدمات تابعين لجهات خارجية (كل حساب من هذا القبيل ، "حساب طرف ثالث") عن طريق: (1) تقديم معلومات تسجيل الدخول إلى حساب الطرف الثالث الخاص بك من خلال الخدمات ؛ أو (2) السماح لنا بالوصول إلى حساب الطرف الثالث الخاص بك ، كما هو مسموح به بموجب الشروط والأحكام المعمول بها التي تحكم استخدامك لكل حساب طرف ثالث. أنت تقر وتضمن أنه يحق لك الكشف عن معلومات تسجيل الدخول إلى حساب الطرف الثالث الخاص بك لنا و / أو منحنا حق الوصول إلى حساب الطرف الثالث الخاص بك ، دون خرق من جانبك لأي من الشروط والأحكام التي تحكم استخدامك لحساب الطرف الثالث المعمول به ، ودون إلزامنا بدفع أي رسوم أو جعلنا خاضعين لأي قيود استخدام يفرضها مزود خدمة الطرف الثالث لحساب الطرف الثالث. من خلال منحنا حق الوصول إلى أي حسابات تابعة لجهات خارجية ، فإنك تدرك أنه (1) يجوز لنا الوصول إلى أي محتوى قدمته وتخزينه في حساب الطرف الثالث الخاص بك ("محتوى الشبكة الاجتماعية") وإتاحته وتخزينه (إن أمكن) بحيث يكون متاحا على الخدمات ومن خلالها عبر حسابك ، بما في ذلك على سبيل المثال لا الحصر أي قوائم أصدقاء و (2) قد نرسل إلى حساب الطرف الثالث الخاص بك ونتلقى منه معلومات إضافية إلى الحد الذي يتم إعلامك عند ربط حسابك بحساب الجهة الخارجية. اعتمادا على حسابات الجهات الخارجية التي تختارها وتخضع لإعدادات الخصوصية التي قمت بتعيينها في حسابات الجهات الخارجية هذه ، قد تكون معلومات التعريف الشخصية التي تنشرها على حسابات الجهات الخارجية الخاصة بك متاحة على حسابك ومن خلاله على الخدمات. يرجى ملاحظة أنه إذا أصبح حساب جهة خارجية أو خدمة مرتبطة به غير متاح أو تم إنهاء وصولنا إلى حساب الطرف الثالث هذا من قبل مزود خدمة الطرف الثالث ، فقد لا يكون محتوى الشبكة الاجتماعية متاحا على الخدمات ومن خلالها. سيكون لديك القدرة على تعطيل الاتصال بين حسابك على الخدمات وحسابات الجهات الخارجية الخاصة بك في أي وقت. يرجى ملاحظة أن علاقتك مع موفري الخدمات الخارجيين المرتبطين بحسابات الجهات الخارجية الخاصة بك تخضع فقط لاتفاقية (اتفاقيات) مع موفري الخدمات التابعين لجهات خارجية. نحن لا نبذل أي جهد لمراجعة أي محتوى شبكة اجتماعية لأي غرض ، بما في ذلك على سبيل المثال لا الحصر ، من أجل الدقة أو الشرعية أو عدم الانتهاك ، ونحن لسنا مسؤولين عن أي محتوى شبكة اجتماعية. أنت تقر وتوافق على أنه يجوز لنا الوصول إلى دفتر عناوين بريدك الإلكتروني المرتبط بحساب جهة خارجية وقائمة جهات الاتصال الخاصة بك المخزنة على جهازك المحمول أو الكمبيوتر اللوحي فقط لأغراض تحديد وإبلاغك بجهات الاتصال التي سجلت أيضا لاستخدام الخدمات. يمكنك إلغاء تنشيط الاتصال بين الخدمات وحساب الطرف الثالث الخاص بك عن طريق الاتصال بنا باستخدام معلومات الاتصال أدناه أو من خلال إعدادات حسابك (إن وجدت). سنحاول حذف أي معلومات مخزنة على خوادمنا تم الحصول عليها من خلال حساب الطرف الثالث هذا ، باستثناء اسم المستخدم وصورة الملف الشخصي التي أصبحت مرتبطة بحسابك.</p>

<h3 id="thirdparty">14. مواقع الطرف الثالث والمحتوى</h3>

<p>قد تحتوي الخدمات (أو قد يتم إرسالك عبر الموقع) على روابط لمواقع ويب أخرى ("مواقع الطرف الثالث") بالإضافة إلى المقالات والصور الفوتوغرافية والنصوص والرسومات والصور والتصميمات والموسيقى والصوت والفيديو والمعلومات والتطبيقات والبرامج والمحتويات أو العناصر الأخرى التي تنتمي إلى أطراف ثالثة أو تنشأ منها ("محتوى الطرف الثالث"). لا يتم التحقيق في مواقع الطرف الثالث ومحتوى الطرف الثالث أو مراقبتها أو التحقق من دقتها أو ملاءمتها أو اكتمالها من قبلنا ، ونحن لسنا مسؤولين عن أي مواقع ويب تابعة لجهات خارجية يتم الوصول إليها من خلال الخدمات أو أي محتوى تابع لجهة خارجية منشور على الخدمات أو متاح من خلالها أو مثبت منها ، بما في ذلك المحتوى أو الدقة أو الإساءة أو الآراء أو الموثوقية أو ممارسات الخصوصية أو السياسات الأخرى أو المضمنة في مواقع الطرف الثالث أو الطرف الثالث محتوى. إن تضمين أو ربط أو السماح باستخدام أو تثبيت أي مواقع ويب تابعة لجهات خارجية أو أي محتوى تابع لجهة خارجية لا يعني موافقتنا أو تأييدنا لها. إذا قررت مغادرة الخدمات والوصول إلى مواقع الطرف الثالث أو استخدام أو تثبيت أي محتوى تابع لجهة خارجية ، فإنك تقوم بذلك على مسؤوليتك الخاصة ، ويجب أن تدرك أن هذه الشروط القانونية لم تعد تحكم. يجب عليك مراجعة الشروط والسياسات المعمول بها ، بما في ذلك ممارسات الخصوصية وجمع البيانات ، لأي موقع ويب تنتقل إليه من الخدمات أو فيما يتعلق بأي تطبيقات تستخدمها أو تثبتها من الخدمات. أي مشتريات تقوم بها من خلال مواقع الطرف الثالث ستكون من خلال مواقع ويب أخرى ومن شركات أخرى ، ونحن لا نتحمل أي مسؤولية من أي نوع فيما يتعلق بهذه المشتريات التي تكون حصريا بينك وبين الطرف الثالث المعمول به. أنت توافق وتقر بأننا لا نؤيد المنتجات أو الخدمات المقدمة على مواقع الطرف الثالث ويجب أن تحملنا المسؤولية عن أي ضرر ناتج عن شرائك لهذه المنتجات أو الخدمات. بالإضافة إلى ذلك ، يجب أن تحملنا المسؤولية عن أي خسائر تتكبدها أو ضرر يلحق بك فيما يتعلق أو ينتج بأي شكل من الأشكال عن أي محتوى تابع لجهة خارجية أو أي اتصال بمواقع الطرف الثالث.</p>

<h3 id="sitemanage">15. إدارة الخدمات</h3>

<p>نحن نحتفظ بالحق ، ولكن ليس الالتزام ، في: (1) مراقبة الخدمات بحثا عن انتهاكات لهذه الشروط القانونية ؛ (2) اتخاذ الإجراءات القانونية المناسبة ضد أي شخص ينتهك ، وفقا لتقديرنا الخاص ، القانون أو هذه الشروط القانونية ، بما في ذلك على سبيل المثال لا الحصر ، الإبلاغ عن هذا المستخدم إلى سلطات إنفاذ القانون ؛ (3) وفقا لتقديرنا الخاص ودون قيود ، رفض أو تقييد الوصول إلى أو تقييد توفر أو تعطيل (إلى الحد الممكن تقنيا) أي من مساهماتك أو أي جزء منها ؛ (4) وفقا لتقديرنا الخاص ودون قيد أو إشعار أو مسؤولية ، لإزالة من الخدمات أو تعطيل جميع الملفات والمحتوى الزائد في الحجم أو التي تشكل بأي شكل من الأشكال عبئا على أنظمتنا ؛ و (5) إدارة الخدمات بطريقة مصممة لحماية حقوقنا وممتلكاتنا وتسهيل الأداء السليم للخدمات.</p>

